    public function show(User $user): View
    {
        $months = now()->diffInDays($user->currentWorkingDate->started_at);

        for ($i = 0; $i < ceil($months / 30); $i++) {
            $dates[] = now()->month(now()->month - $i);
        }

        return view('users.show', compact('user', 'dates'));
    }

