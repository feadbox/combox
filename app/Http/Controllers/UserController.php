    public function index(Request $request): View
    {
        $users = User::query()
            ->where(function ($query) use ($request) {
                filled($request->q) ? $query->search($request->q) : $query->stillWorking();
            })
            ->oldest('first_name')
            ->paginate();

        return view('users.index', [
            'users' => $users,
        ]);
    }

